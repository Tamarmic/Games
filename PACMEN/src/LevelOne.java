public void addLevelInfo (Play play, int[] levelObject){

for (int i =0 ;i<400; i++){
    if (levelObject[i]==2||levelObject[i]==7){
        blocks.add(new Block(new Point(31*(i%20),31*(i/20)),30,30, Color.cyan));
    }
    if (levelObject[i]==3){
        blocks.add(new Block(new Point(31*(i%20),31*(i/20)),30,30, Color.pink));
    }
    if (levelObject[i]!=2&&levelObject[i]!=7&&levelObject[i]!=3){
        gamePoints.add(new GamePoint(new Point(31*(i%20)+15,31*(i/20)+15),play));
    }
    if (levelObject[i]==4){
        monsters.add(new S(new Point(31*(i%20)+15,31*(i/20)+15),getMonsterColor()));
    }
}
}
public Color getMonsterColor(){

     if (color==1){
         color++;
         return Color.pink;
     }
    if (color==2){
        color++;
        return Color.green;
    } if (color==3){
        color++;
        return Color.blue;
    } else {
        return Color.red;
    }
}
